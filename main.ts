game.onUpdate(function () {
    if (menu == false) {
        characterPlayer.vx = Speed
        if (characterState == "Cube") {
            if (controller.A.isPressed() && !(characterPlayer.isHittingTile(CollisionDirection.Top))) {
                if (characterPlayer.vy == 0) {
                    if (characterPlayer.ay == 400) {
                        characterPlayer.vy = -157
                    } else if (characterPlayer.ay == -400) {
                        characterPlayer.vy = 157
                    }
                }
            }
        } else if (characterState == "Ship") {
            if (controller.A.isPressed()) {
                characterPlayer.ay = -200
            } else {
                characterPlayer.ay = 200
            }
        }
        if (characterPlayer.tilemapLocation().column == 32) {
            color.setColor(3, color.parseColorString("#7CFF4D"))
            color.setColor(10, color.parseColorString("#47ffaf"))
            color.setColor(11, color.parseColorString("#1FD63D"))
            color.setColor(12, color.parseColorString("#0DAB17"))
            color.setColor(13, color.parseColorString("#7CE67A"), 100)
        } else if (characterPlayer.tilemapLocation().column == 64) {
            color.setColor(3, color.parseColorString("#FF944D"))
            color.setColor(10, color.parseColorString("#FF4747"))
            color.setColor(11, color.parseColorString("#D61F1F"))
            color.setColor(12, color.parseColorString("#AB0D0D"))
            color.setColor(13, color.parseColorString("#E6977A"), 100)
        } else {
        	
        }
        scene.centerCameraAt(characterPlayer.x + 40, characterPlayer.y)
        if (characterState == "Cube") {
            characterAnimations.loopFrames(
            characterPlayer,
            assets.animation`Roll0`,
            100,
            characterAnimations.rule(Predicate.MovingUp)
            )
            characterAnimations.loopFrames(
            characterPlayer,
            assets.animation`Roll0`,
            100,
            characterAnimations.rule(Predicate.MovingDown)
            )
        } else if (characterState == "Ship") {
            characterAnimations.loopFrames(
            characterPlayer,
            assets.animation`shipForward`,
            100,
            characterAnimations.rule(Predicate.MovingUp)
            )
            characterAnimations.loopFrames(
            characterPlayer,
            assets.animation`shipForward`,
            100,
            characterAnimations.rule(Predicate.MovingDown)
            )
        }
        if (characterPlayer.tilemapLocation().column > 222.5) {
            tiles.placeOnTile(characterPlayer, tiles.getTileLocation(23, characterPlayer.tilemapLocation().row))
        }
    }
    for (let value of sprites.allOfKind(SpriteKind.Bpad)) {
        if (value.x < scene.cameraProperty(CameraProperty.X) - 80) {
            sprites.destroy(value)
        }
    }
    for (let value of sprites.allOfKind(SpriteKind.BpadFlipped)) {
        if (value.x < scene.cameraProperty(CameraProperty.X) - 80) {
            sprites.destroy(value)
        }
    }
})
